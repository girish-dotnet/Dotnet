/* Number functions */ 

abs() : Returns absolute value 

select abs(-12) 

ceiling() : Returns the greatest integer value 

select CEILING(12.0000001)

FLOOR() : Returns the smallest integer value 

SELECT FLOOR(12.999999) 

power(n,m) : Returns n powerm vlaue 

select power(2,3) 

sqrt() : returns the sqrt value 

select SQRT(49)

round() : used to round the digit w.r.t precision specified 

select round(8767.8678,3) 
select round(8767.8678,2) 


select round(7543.7774,-2) 








